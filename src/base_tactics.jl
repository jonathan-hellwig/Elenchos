# Functions for building and transforming complete proofs (Derivation values).
# These are *forward* combinators — they compose via `|>` to construct proof
# objects, as opposed to backward tactics which reduce open subgoals.

"""    start_proof(f::Formula) -> Derivation
    start_proof(s::Sequent) -> Derivation

Create an initial proof state from a goal formula or sequent.
"""
start_proof(f::Formula) = Derivation(Assume(Sequent(f)))
start_proof(s::Sequent) = Derivation(Assume(s))

"""    fwd_axiom(ax::KernelAxiom) -> Derivation
    fwd_axiom(ax, σ) -> Derivation
    fwd_axiom(ax, pairs...) -> Derivation

Create and optionally instantiate an axiom.
"""
fwd_axiom(ax::KernelAxiom) = Derivation(AxiomRule(ax))
fwd_axiom(ax::KernelAxiom, σ::Substitution) = instantiate(fwd_axiom(ax), σ)
fwd_axiom(ax::KernelAxiom, pairs::Union{Pair{FunctionSymbol,<:Term},Pair{PredicateSymbol,<:Formula}}...) =
    fwd_axiom(ax, Substitution(pairs...))

"""    instantiate(proof, σ) -> Derivation
    instantiate(proof, pairs...) -> Derivation

Apply a substitution to a complete proof, yielding a new proof with
schematic variables replaced.
"""
function instantiate(proof::Derivation, σ::Substitution)::Derivation
    sub = Derivation(SubstitutionRule(proof.conclusion, σ))
    Derivation(sub, 1, proof)
end

function instantiate(proof::Derivation, pairs::Union{Pair{FunctionSymbol,<:Term},Pair{PredicateSymbol,<:Formula}}...)::Derivation
    instantiate(proof, Substitution(pairs...))
end

"""    fwd_implies_right(proof::Derivation) -> Derivation

Convert a proof of `Γ ⊢ A → B` into a proof of `Γ, A ⊢ B`.

Can be chained to unwrap nested implications:

    fwd_implies_right(fwd_implies_right(fwd_axiom(AX_CONG_PRED, σ)))
    # ⊢ (x=y) → (P(x) → P(y))  ──▶  (x=y), P(x) ⊢ P(y)
"""
function fwd_implies_right(proof::Derivation)::Derivation
    isempty(proof.assumptions) ||
        error("fwd_implies_right: proof must be complete (no open assumptions)")
    length(proof.conclusion.consequent) == 1 ||
        error("fwd_implies_right: proof must have exactly one consequent")
    impl = proof.conclusion.consequent[1]
    impl isa Implies ||
        error("fwd_implies_right: consequent must be an implication, got $(typeof(impl))")

    Γ = proof.conclusion.antecedent
    n = length(Γ)
    A, B = impl.left, impl.right
    goal = Sequent((Γ..., A), (B,))

    # Cut on A → B:  sub1 = Γ, A ⊢ B, A→B  ;  sub2 = Γ, A, A→B ⊢ B
    cut = Derivation(CutRule(goal, impl, Cons(2), Ante(n + 2)))

    # --- Close sub1: Γ, A ⊢ B, A→B ---
    # Weaken away A from antecedent (position n+1), then weaken B from consequent,
    # to reach Γ ⊢ A→B  which matches the original proof's conclusion.
    current = cut.assumptions[1]              # Γ, A ⊢ B, A→B
    wl = Derivation(WeakeningLeftRule(current, Ante(n + 1)))
    # └─ Γ ⊢ B, A→B
    wr = Derivation(WeakeningRightRule(wl.assumptions[1], Cons(1)))
    # └─ Γ ⊢ A→B
    closed_sub1 = Derivation(wl, 1, Derivation(wr, 1, proof))

    # --- Close sub2: Γ, A, A→B ⊢ B ---
    # ImpliesLeft on A→B (position n+2):
    #   branch 1 = Γ, A ⊢ B, A  ;  branch 2 = Γ, A, B ⊢ B
    il = Derivation(ImpliesLeftRule(cut.assumptions[2], Ante(n + 2), Cons(length(cut.assumptions[2].consequent))))
    close_a = Derivation(CloseRule(il.assumptions[1], Ante(n + 1), Cons(1)))
    close_b = Derivation(CloseRule(il.assumptions[2], Ante(n + 2), Cons(1)))
    closed_sub2 = Derivation(Derivation(il, 1, close_a), 1, close_b)

    # Combine
    Derivation(Derivation(cut, 1, closed_sub1), 1, closed_sub2)
end

"""    rule(ax, σ) -> Derivation
    rule(ax, pairs...) -> Derivation
    rule(ax) -> Derivation

Instantiate a conditional axiom and convert to rule normal form by
repeatedly assuming premises.

    ⊢ A → (B → C)   ──rule──▶   A, B ⊢ C

Examples:

    rule(AX_CONG_PRED, _x_sym => a, _y_sym => b, _P_sym => ctx)
    #   ⊢ (a=b) → (P(a) → P(b))   ──▶   (a=b), P(a) ⊢ P(b)
"""
function rule(ax::KernelAxiom, args...)::Derivation
    proof = fwd_axiom(ax, args...)
    return rule(proof)
end

function rule(proof::Derivation, args...)::Derivation
    while !isempty(proof.conclusion.consequent) && proof.conclusion.consequent[1] isa Implies
        proof = fwd_implies_right(proof)
    end
    return proof
end

