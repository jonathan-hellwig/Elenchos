    Derivation(p, subgoal_idx, Derivation(ForallRightRule(seq, tidx, fresh)))
end

function Tactics.execute(t::ForallLeft, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_ante(t.target_idx, seq, f -> f isa Forall)
    fml = seq.antecedent[tidx.idx]::Forall


    inst_formula = Implies(fml, substitute(fml.body, Substitution(fml.variable => t.term)))

    # 1. Cut the implication
    # Subgoal 1: Γ, ∀x. P(x) ⊢ (∀x. P(x)) → P(t), Δ
    # Subgoal 2: Γ, ∀x. P(x), (∀x. P(x)) → P(t) ⊢ Δ

    m = length(seq.antecedent)
    num_cons = length(seq.consequent)

    p = p |> cut(inst_formula; insert_ante=Ante(m + 1), insert_cons=Cons(num_cons + 1), subgoal_idx=subgoal_idx)

    # Subgoal 1 (now at index subgoal_idx):
    # Close it with the axiom
    ax_proof = fwd_axiom(AX_FORALL_INST, _x => fml.variable, _P_sym => fml.body, _y => t.term)
    p = p |> exact(ax_proof; subgoal_idx=subgoal_idx)

    # Subgoal 2 (now at index subgoal_idx):
    # seq2 = Γ, ∀x. P(x), (∀x. P(x)) → P(t) ⊢ Δ
    # Apply ImpliesLeft on the implication
    # Subgoal 2.1: Γ, ∀x. P(x) ⊢ ∀x. P(x), Δ  (Close with Id)
    # Subgoal 2.2: Γ, ∀x. P(x), P(t) ⊢ Δ      (The desired new proof state)

    # We need to resolve the new implication index in the antecedent (it's at m + 1)
    p = p |> implies_left(target_idx=Ante(m + 1), subgoal_idx=subgoal_idx)

    # Subgoal 2.1: Close with identity
    p = p |> Id(subgoal_idx=subgoal_idx)

    return p
end

