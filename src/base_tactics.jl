function Tactics.execute(::Id, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    for (ci, goal) in enumerate(seq.consequent)
        for (ai, hyp) in enumerate(seq.antecedent)
            if hyp == goal
                rule = CloseRule(seq, Ante(ai), Cons(ci))
                return Derivation(p, subgoal_idx, Derivation(rule))
            end
        end
    end
    throw(TacticFailure("No matching hypothesis found"))
end

