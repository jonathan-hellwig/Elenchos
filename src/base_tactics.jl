"""
    generate_fresh_variable(base::Variable, avoid::Set{Variable}) -> Variable

Generate a fresh variable that is not in the `avoid` set.
Uses the base variable's name, appending _1, _2, etc. if needed.
"""
function generate_fresh_variable(base::Variable, avoid::Set{Variable})::Variable
    name = base.name
    if Variable(name) ∉ avoid
        return Variable(name)
    end
    counter = 1
    while true
        candidate = Variable("$(base.name)_$counter")
        if candidate ∉ avoid
            return candidate
        end
        counter += 1
    end
end

