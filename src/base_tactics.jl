function Tactics.execute(t::AndLeft, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_ante(t.target_idx, seq, f -> is_and_pattern(f) !== nothing)

    # A ∧ B = ¬(A → ¬B) — three kernel steps
    step1 = Derivation(NotLeftRule(seq, Ante(tidx.idx), Cons(length(seq.consequent) + 1)))
    seq1 = step1.assumptions[1]
    step2 = Derivation(ImpliesRightRule(seq1, Cons(length(seq1.consequent)), Ante(length(seq1.antecedent) + 1)))
    seq2 = step2.assumptions[1]
    step3 = Derivation(NotRightRule(seq2, Cons(length(seq2.consequent)), Ante(length(seq2.antecedent) + 1)))
    result = Derivation(step1, 1, Derivation(step2, 1, step3))
    Derivation(p, subgoal_idx, result)
end

function Tactics.execute(t::AndRight, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_cons(t.target_idx, seq, f -> is_and_pattern(f) !== nothing)

    step1 = Derivation(NotRightRule(seq, Cons(tidx.idx), Ante(length(seq.antecedent) + 1)))
    seq1 = step1.assumptions[1]
    step2 = Derivation(ImpliesLeftRule(seq1, Ante(length(seq1.antecedent)), Cons(length(seq1.consequent) + 1)))
    seq2 = step2.assumptions[2]
    step3 = Derivation(NotLeftRule(seq2, Ante(length(seq2.antecedent)), Cons(length(seq2.consequent) + 1)))
    result = Derivation(step1, 1, Derivation(step2, 2, step3))
    Derivation(p, subgoal_idx, result)
end

function Tactics.execute(t::OrRight, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_cons(t.target_idx, seq, f -> is_or_pattern(f) !== nothing)

    step1 = Derivation(ImpliesRightRule(seq, Cons(tidx.idx), Ante(length(seq.antecedent) + 1)))
    seq1 = step1.assumptions[1]
    step2 = Derivation(NotLeftRule(seq1, Ante(length(seq1.antecedent)), Cons(length(seq1.consequent) + 1)))
    result = Derivation(step1, 1, step2)
    Derivation(p, subgoal_idx, result)
end

function Tactics.execute(t::OrLeft, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_ante(t.target_idx, seq, f -> is_or_pattern(f) !== nothing)

    # A ∨ B = ¬A → B
    # ImpliesLeft on ¬A → B gives:
    # 1. Γ ⊢ ¬A, Δ
    # 2. Γ, B ⊢ Δ
    step1 = Derivation(ImpliesLeftRule(seq, Ante(tidx.idx), Cons(length(seq.consequent) + 1)))
    seq1_1 = step1.assumptions[1]

    # NotRight on Premise 1 gives: Γ, A ⊢ Δ
    step2 = Derivation(NotRightRule(seq1_1, Cons(length(seq1_1.consequent)), Ante(length(seq1_1.antecedent) + 1)))

    # Combined result
    result = Derivation(step1, 1, step2)
    Derivation(p, subgoal_idx, result)
end


function Tactics.execute(t::Cut, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    ins_ante = if t.insert_ante !== nothing
        t.insert_ante
    else
        Ante(length(seq.antecedent) + 1)
    end
    ins_cons = if t.insert_cons !== nothing
        t.insert_cons
    else
        Cons(length(seq.consequent) + 1)
    end
    Derivation(p, subgoal_idx, Derivation(CutRule(seq, t.formula, ins_cons, ins_ante)))
end


function Tactics.execute(t::WeakenLeft, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    idx = if t.idx !== nothing
        t.idx.idx
    elseif t.fml !== nothing
        findfirst(f -> f == t.fml, seq.antecedent)
    else
        throw(TacticFailure("No target (fml or idx) specified for WeakenLeft"))
    end
    idx === nothing && throw(TacticFailure("Target not found in antecedent: $(t.fml)"))
    return Derivation(p, subgoal_idx, Derivation(WeakeningLeftRule(seq, Ante(idx))))
end

function Tactics.execute(t::WeakenRight, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    idx = if t.idx !== nothing
        t.idx.idx
    elseif t.fml !== nothing
        findfirst(f -> f == t.fml, seq.consequent)
    else
        throw(TacticFailure("No target (fml or idx) specified for WeakenRight"))
    end
    idx === nothing && throw(TacticFailure("Target not found in consequent: $(t.fml)"))
    return Derivation(p, subgoal_idx, Derivation(WeakeningRightRule(seq, Cons(idx))))
end

function Tactics.execute(t::Exact, p::Derivation, subgoal_idx::Int)::Derivation
    Derivation(p, subgoal_idx, t.proof)
end

function exact(ax::KernelAxiom; subgoal_idx=1, σ::Union{Substitution,Nothing}=nothing)
    if σ !== nothing
        exact(fwd_axiom(ax, σ); subgoal_idx=subgoal_idx)
    else
        exact(fwd_axiom(ax); subgoal_idx=subgoal_idx)
    end
end

function Tactics.execute(t::Subst, p::Derivation, subgoal_idx::Int)::Derivation
    instantiated_rule = Derivation(SubstitutionRule(t.seq, t.σ))
    Derivation(p, subgoal_idx, instantiated_rule)
end

function Tactics.execute(t::WeakenAllBut, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    # Weaken antecedent
    ante_to_weaken = [fml for fml in seq.antecedent if fml != t.pred]
    for duplicate in ante_to_weaken
        p = p |> weaken_left(fml=duplicate, subgoal_idx=subgoal_idx)
    end
    # Weaken consequent
    cons_to_weaken = [fml for fml in seq.consequent if fml != t.pred]
    for duplicate in cons_to_weaken
        p = p |> weaken_right(fml=duplicate, subgoal_idx=subgoal_idx)
    end
    return p
end

