"""
    term_lt(a::Term, b::Term) -> Bool

Total order on terms for canonical sorting of commutative sums/products.

Ordering: Constants < Variables (by name) < compound terms (by constructor tag,
then lexicographically by children).
"""
function term_lt(a::Term, b::Term)
    ta, tb = _term_tag(a), _term_tag(b)
    ta != tb && return ta < tb
    _term_lt_same_tag(a, b)
end

# Assign numeric tags for top-level ordering
_term_tag(::Constant) = 0
_term_tag(::Variable) = 1
_term_tag(::FunctionApplication) = 2
_term_tag(::Sum) = 3
_term_tag(::Difference) = 4
_term_tag(::Product) = 5
_term_tag(::Division) = 6
_term_tag(::Power) = 7
_term_tag(::Hole) = 8

# Same-tag comparison
_term_lt_same_tag(a::Constant, b::Constant) = a.value < b.value
_term_lt_same_tag(a::Variable, b::Variable) = a.name < b.name

function _term_lt_same_tag(a::FunctionApplication, b::FunctionApplication)
    a.symbol.name != b.symbol.name && return a.symbol.name < b.symbol.name
    _lex_lt(a.arguments, b.arguments)
end

function _term_lt_same_tag(a::T, b::T) where T<:Union{Sum,Difference,Product,Division}
    a.left != b.left && return term_lt(a.left, b.left)
    term_lt(a.right, b.right)
end

function _term_lt_same_tag(a::Power, b::Power)
    a.base != b.base && return term_lt(a.base, b.base)
    term_lt(a.exponent, b.exponent)
end

_term_lt_same_tag(a::Hole, b::Hole) = a.index < b.index

function _lex_lt(as, bs)
    for (a, b) in zip(as, bs)
        a != b && return term_lt(a, b)
    end
    length(as) < length(bs)
end

export term_lt

# ─────────────────────────────────────────────────────────────────────────────
#                       ORDERING FUNCTORS
# ─────────────────────────────────────────────────────────────────────────────

abstract type Order end
struct NoOrder <: Order end
struct TermOrder <: Order end
struct MonomialOrder <: Order end

"""
    split_coeff(t::Term) -> (Constant, Term)

Split a summand into (coefficient, monomial_body).
After right-association of products the coefficient is the leftmost factor
when it is a Constant; otherwise the implicit coefficient is 1.
"""
function split_coeff(t::Term)::Tuple{Constant,Term}
    if t isa Product && t.left isa Constant
        return (t.left, t.right)
    end
    return (ONE, t)
end

"""
    monomial_lt(a::Term, b::Term) -> Bool

Lexicographic monomial order for sorting summands in a polynomial.
Strips leading numeric coefficients and compares monomial bodies via
`term_lt`; ties are broken by coefficient.
Sums are compared recursively by their head summand.
"""
function monomial_lt(a::Term, b::Term)::Bool
    # Recursive comparison for right-associated sums
    if a isa Sum && b isa Sum
        return a.left == b.left ? monomial_lt(a.right, b.right) : monomial_lt(a.left, b.left)
    end

    # A non-sum is always "smaller" than a sum (atoms sort before tails)
    a isa Sum && return false
    b isa Sum && return true

    # Compare individual summands by monomial body, then coefficient
    ca, ma = split_coeff(a)
    cb, mb = split_coeff(b)

    return ma == mb ? term_lt(ca, cb) : term_lt(ma, mb)
end

# Functor interface: (order)(lower, higher) -> Bool
# Strongly typed to prevent dynamic dispatch when called inside the unification loop
(::NoOrder)(a::Term, b::Term) = true
(::TermOrder)(a::Term, b::Term) = term_lt(a, b)
(::MonomialOrder)(a::Term, b::Term) = monomial_lt(a, b)

