struct FindTarget{T<:Union{SequentPosition,First,Nothing},O<:Order}
    pattern::Term
    pos::T
    order::O
end

struct ExactSubterm{T<:Union{SequentPosition,First,Nothing}}
    pos::T
    path::Vector{Int}
end

struct ExactMatch{T<:Union{SequentPosition,First,Nothing}}
    pattern::Term
    pos::T
end

function resolve_site(loc::ExactMatch, seq::Sequent, replacement::Term)
    search_pos = loc.pos isa First ? nothing : loc.pos
    site = find_first_in_sequent(seq; pos=search_pos) do term
        term == loc.pattern ? Substitution() : nothing
    end
    if site === nothing
        pos_info = loc.pos === nothing ? "" : " at position $(loc.pos)"
        throw(TacticFailure("Pattern $(loc.pattern) not found$pos_info with ExactMatch in sequent.\n$seq"))
    end
    _, ctx = site.data
    return loc.pattern, replacement, ctx, site.pos
end

function resolve_site(loc::FindTarget, seq::Sequent, replacement::Term)
    check_order = !(loc.order isa NoOrder)
    search_pos = loc.pos isa First ? nothing : loc.pos
    site = find_first_in_sequent(seq; pos=search_pos) do term
        σ = unify(loc.pattern, term)
        σ === nothing && return nothing
        !check_order || loc.order(substitute(replacement, σ), substitute(loc.pattern, σ)) ? σ : nothing
    end
    if site === nothing
        pos_info = loc.pos === nothing ? "" : " at position $(loc.pos)"
        throw(TacticFailure("Pattern $(loc.pattern) not found$pos_info with $(typeof(loc.order)) in sequent.\n$seq"))
    end
    σ, ctx = site.data
    return σ(loc.pattern), σ(replacement), ctx, site.pos
end

using ..Walkers: path_context
function resolve_site(loc::ExactSubterm, seq::Sequent, replacement::Term)
    pos = loc.pos
    if pos isa First || pos === nothing
        pos = Cons(1)
    end
    fml = pos isa Ante ? seq.antecedent[pos.idx] : seq.consequent[pos.idx]
    ctx, old = path_context(fml, loc.path)
    return old, replacement, ctx, pos
end

struct Rewrite{L} <: Tactics.Tactic
    replacement::Term
    loc::L
    subgoal_idx::Union{Int,Nothing}
end

# User APIs
rewrite(replace::Pair; seq_pos::Union{SequentPosition,First,Nothing}=First(), order::Order=NoOrder(), subgoal_idx=1) =
    Rewrite(convert(Term, replace.second), FindTarget(convert(Term, replace.first), seq_pos, order), subgoal_idx)

rewrite(replace::Pair, ::Type{ExactMatch}; seq_pos::Union{SequentPosition,First,Nothing}=First(), subgoal_idx=1) =
    Rewrite(convert(Term, replace.second), ExactMatch(convert(Term, replace.first), seq_pos), subgoal_idx)

rewrite(replacement::Term, seq_pos::SequentPosition, path::Vector{Int}; subgoal_idx=1) =
    Rewrite(replacement, ExactSubterm(seq_pos, path), subgoal_idx)

rewrite(replacement::Term, path::Vector{Int}; subgoal_idx=1) =
    Rewrite(replacement, ExactSubterm(Cons(1), path), subgoal_idx)

rewrite_left(term::Term, seq_pos::SequentPosition; kwargs...) = rewrite(term, seq_pos, [1]; kwargs...)
rewrite_left(term::Term; kwargs...) = rewrite(term, [1]; kwargs...)

rewrite_right(term::Term, seq_pos::SequentPosition; kwargs...) = rewrite(term, seq_pos, [2]; kwargs...)
rewrite_right(term::Term; kwargs...) = rewrite(term, [2]; kwargs...)

Base.show(io::IO, r::Rewrite) = print(io, "rewrite")

function (t::Rewrite)(p::Derivation, subgoal_idx::Int)
    n = length(p.assumptions)
    1 <= subgoal_idx <= n || throw(Tactics.TacticFailure("Subgoal $subgoal_idx out of bounds ($n open)"))
    Tactics.execute(t, p, subgoal_idx)
end

function Tactics.execute(t::Rewrite, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    old_term, new_term, ctx, pos = resolve_site(t.loc, seq, t.replacement)

    if pos isa Cons
        ν = Substitution(_P_sym => ctx, _x_sym => new_term, _y_sym => old_term)
        return p |> apply(instantiate(rule(AX_CONG_PRED), ν); target_idx=pos, subgoal_idx=subgoal_idx)
    else
        η = Substitution(1 => new_term)
        new_fml = substitute(ctx, η)
        idx_cons = Cons(length(seq.consequent) + 1)
        p = p |> cut(new_fml; subgoal_idx=subgoal_idx, insert_cons=idx_cons)

        ν_b = Substitution(_P_sym => ctx, _x_sym => old_term, _y_sym => new_term)
        p = p |> apply(instantiate(rule(AX_CONG_PRED), ν_b); target_idx=idx_cons, subgoal_idx=subgoal_idx)

        # Close the P(old_term) branch which is now at subgoal_idx + 1
        p = p |> id(subgoal_idx=subgoal_idx + 1)

        # Weaken old_fml from the assumed new_fml branch, now at subgoal_idx + 1 again
        fml_old = pos isa Ante ? seq.antecedent[pos.idx] : seq.consequent[pos.idx]
        p = p |> weaken_left(fml=fml_old; subgoal_idx=subgoal_idx + 1)
        
        return p
    end
end

