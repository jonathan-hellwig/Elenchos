function Tactics.execute(t::Weaken, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    # Check antecedent
    idx_ante = findfirst(f -> f == t.fml, seq.antecedent)
    if idx_ante !== nothing
        return Derivation(p, subgoal_idx, Derivation(WeakeningLeftRule(seq, Ante(idx_ante))))
    end

    # Check consequent
    idx_cons = findfirst(f -> f == t.fml, seq.consequent)
    if idx_cons !== nothing
        return Derivation(p, subgoal_idx, Derivation(WeakeningRightRule(seq, Cons(idx_cons))))
    end

    throw(TacticFailure("Formula $(t.fml) not found in sequent"))
end

# ── Arithmetic ────────────────────────────────────────────────────────────────

function Tactics.execute(t::ArithRight, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_cons(t.target_idx, seq, f -> f isa LessThan)
    lt = seq.consequent[tidx.idx]

    # Needs to match the existing context the same way ArithLeft does.
    # GroundInequalityRightRule produces `⊢ a < b`
    current = seq
    weakenings = Derivation[]

    # Remove consequent formulas other than tidx
    adjusted_tidx = tidx.idx
    for j in length(current.consequent):-1:1
        j == adjusted_tidx && continue
        w = Derivation(WeakeningRightRule(current, Cons(j)))
        push!(weakenings, w)
        current = w.assumptions[1]
        if j < adjusted_tidx
            adjusted_tidx -= 1
        end
    end

    # Remove all antecedent formulas
    for j in length(current.antecedent):-1:1
        w = Derivation(WeakeningLeftRule(current, Ante(j)))
        push!(weakenings, w)
        current = w.assumptions[1]
    end

    closed = Derivation(GroundInequalityRightRule(lt.left, lt.right))
    for w in reverse(weakenings)
        closed = Derivation(w, 1, closed)
    end

    Derivation(p, subgoal_idx, closed)
end

function Tactics.execute(t::ArithLeft, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_ante(t.target_idx, seq, f -> f isa LessThan)
    lt = seq.antecedent[tidx.idx]

    # GroundInequalityLeftRule produces a bare  `a < b ⊢`  with no other
    # formulas.  We must weaken away every extra antecedent and every
    # consequent formula so the kernel proof matches the subgoal.
    current = seq
    weakenings = Derivation[]

    # Remove consequent formulas (right-to-left)
    for j in length(current.consequent):-1:1
        w = Derivation(WeakeningRightRule(current, Cons(j)))
        push!(weakenings, w)
        current = w.assumptions[1]
    end

    # Remove antecedent formulas other than tidx (right-to-left, adjusting index)
    adjusted_tidx = tidx.idx  # tracks where our target moved
    for j in length(current.antecedent):-1:1
        j == adjusted_tidx && continue
        w = Derivation(WeakeningLeftRule(current, Ante(j)))
        push!(weakenings, w)
        current = w.assumptions[1]
        if j < adjusted_tidx
            adjusted_tidx -= 1
        end
    end

    # Now current == `a < b ⊢`, which matches GroundInequalityLeftRule output
    closed = Derivation(GroundInequalityLeftRule(lt.left, lt.right))
    for w in reverse(weakenings)
        closed = Derivation(w, 1, closed)
    end

    Derivation(p, subgoal_idx, closed)
end

function Tactics.execute(t::Comm, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    tidx = resolve_cons(t.target_idx, seq, f -> f isa Equals)
    fml = seq.consequent[tidx.idx]

    if (σ = unify(_x + _y ~ _y + _x, fml)) !== nothing
        return p |> apply(instantiate(rule(AX_ADD_COMM), σ); target_idx=tidx, subgoal_idx=subgoal_idx)
    elseif (σ = unify(_x * _y ~ _y * _x, fml)) !== nothing
        return p |> apply(instantiate(rule(AX_MUL_COMM), σ); target_idx=tidx, subgoal_idx=subgoal_idx)
    end

    throw(TacticFailure("Could not match commutativity pattern for goal $(fml)"))
end

function Tactics.execute(t::ApplyRule, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]
    rule_deriv = t.rule
    isempty(rule_deriv.assumptions) || throw(TacticFailure("Rule must be complete (no open assumptions)"))

    num_rule_cons = length(rule_deriv.conclusion.consequent)
    num_rule_cons <= 1 ||
        throw(TacticFailure("Rule must have at most one formula in consequent"))

    premises = rule_deriv.conclusion.antecedent
    n_prem = length(premises)
    m = length(seq.antecedent)
    num_cons = length(seq.consequent)

    if num_rule_cons == 1
        B = rule_deriv.conclusion.consequent[1]
        tidx = if t.target_idx !== nothing
            t.target_idx
        else
            idx = findfirst(f -> f == B, seq.consequent)
            idx === nothing && throw(TacticFailure("Rule conclusion $B not found in consequent"))
            idx
        end
        1 <= tidx <= num_cons || throw(BoundsError(seq.consequent, tidx))
        seq.consequent[tidx] == B ||
            throw(TacticFailure("Formula at target index doesn't match rule conclusion"))
    else
        tidx = nothing
    end

    # Apply the tactical pipeline explicitly using existing combinators
    for (i, A) in enumerate(premises)
        p = p |> cut(A; insert_ante=Ante(m + i), insert_cons=Cons(num_cons + 1), subgoal_idx=subgoal_idx + i - 1)

        for j in num_cons:-1:1
            p = p |> weaken_right(idx=Cons(j), subgoal_idx=subgoal_idx + i - 1)
        end

        for j in (i-1):-1:1
            p = p |> weaken_left(idx=Ante(m + j), subgoal_idx=subgoal_idx + i - 1)
        end
    end

    final_idx = subgoal_idx + n_prem
    for j in num_cons:-1:1
        if tidx === nothing || j != tidx
            p = p |> weaken_right(idx=Cons(j), subgoal_idx=final_idx)
        end
    end
    for j in m:-1:1
        p = p |> weaken_left(idx=Ante(j), subgoal_idx=final_idx)
    end

    return p |> exact(rule_deriv; subgoal_idx=final_idx)
end

function Tactics.execute(t::Apply, p::Derivation, subgoal_idx::Int)::Derivation
    seq = p.assumptions[subgoal_idx]

    base = t.target isa KernelAxiom ? rule(t.target) : t.target
    if t.σ !== nothing
        base = instantiate(base, t.σ)
    end

    candidates = t.target isa KernelAxiom ? (base,) : (base, rule(base))
    last_err = nothing

    for cand in candidates
        try
            isempty(cand.assumptions) || throw(TacticFailure("Rule must be complete (no open assumptions)"))
            length(cand.conclusion.consequent) == 1 || throw(TacticFailure("Rule must have exactly one formula in consequent"))

            rule_conclusion = cand.conclusion.consequent[1]
            tidx = resolve_cons(t.target_idx, seq, f -> unify(rule_conclusion, f) !== nothing)
            goal_fml = seq.consequent[tidx.idx]
            σ_unify = unify(rule_conclusion, goal_fml)
            σ_unify === nothing && throw(TacticFailure("Cannot unify rule conclusion $(rule_conclusion) with goal $(goal_fml)"))

            instantiated = instantiate(cand, σ_unify)
            return Tactics.execute(apply_rule(instantiated; target_idx=tidx.idx), p, subgoal_idx)
        catch err
            last_err = err
        end
    end
    if last_err isa Exception
        throw(last_err)
    end
    throw(TacticFailure("Could not apply rule"))
end

